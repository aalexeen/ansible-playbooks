#!/usr/bin/env bash
# =============================================================
# install.sh / tacacs-ng.sh — Installer & manager for tacacs-ng
# =============================================================
# Two operating modes detected automatically:
#
#   SOURCE mode  — run from the cloned repository (roles/ present)
#                  Commands: install, status, help
#
#   INSTALLED mode — run from the installed playbook directory
#                    (.installed marker present, or copied as tacacs-ng.sh)
#                    Commands: configure, configure-host, configure-users,
#                              configure-vault-password, run, status,
#                              uninstall, help
#
# On install, this script copies itself to INSTALL_DIR/tacacs-ng.sh
# so the user can manage the deployment from there without the source repo.
# =============================================================
set -euo pipefail

# ---------- colours ------------------------------------------
RED='\033[0;31m'; GREEN='\033[0;32m'; YELLOW='\033[1;33m'
CYAN='\033[0;36m'; BOLD='\033[1m'; NC='\033[0m'

# ---------- paths --------------------------------------------
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
SCRIPT_NAME="$(basename "${BASH_SOURCE[0]}")"
PLAYBOOK_NAME="tacacs-ng"
VAULT_FILE_REL="roles/tac_plus_ng_deploy/vars/vault.yml"

# ---------- helpers ------------------------------------------
info()    { echo -e "${CYAN}[INFO]${NC}  $*"; }
ok()      { echo -e "${GREEN}[OK]${NC}    $*"; }
warn()    { echo -e "${YELLOW}[WARN]${NC}  $*"; }
error()   { echo -e "${RED}[ERROR]${NC} $*" >&2; }
die()     { error "$*"; exit 1; }
header()  { echo -e "\n${BOLD}=== $* ===${NC}"; }
prompt() {
    local var="$1" desc="$2" default="${3:-}"
    local input
    if [[ -n "$default" ]]; then
        echo -ne "${BOLD}${desc}${NC} [${default}]: "
    else
        echo -ne "${BOLD}${desc}${NC}: "
    fi
    read -r input || { echo; _on_interrupt; }
    [[ -z "$input" && -n "$default" ]] && input="$default"
    printf -v "$var" '%s' "$input"
}

prompt_secret() {
    local var="$1" desc="$2"
    local input
    echo -ne "${BOLD}${desc}${NC} (hidden): "
    read -rs input || { echo; _on_interrupt; }
    echo
    printf -v "$var" '%s' "$input"
}

confirm() {
    echo -ne "${BOLD}${1}${NC} [y/N]: "
    local r
    read -r r || { echo; _on_interrupt; }
    if [[ "$r" =~ ^[Yy]$ ]]; then return 0; else return 1; fi
}

# ---------- mode detection -----------------------------------
detect_mode() {
    if [[ -f "${SCRIPT_DIR}/.installed" ]]; then
        echo "installed"
    elif [[ -d "${SCRIPT_DIR}/roles" ]]; then
        echo "source"
    else
        echo "installed"   # fallback: assume deployed context
    fi
}

detect_default_location() {
    if [[ $EUID -eq 0 ]]; then
        echo "/etc/ansible/playbooks/${PLAYBOOK_NAME}"
    else
        echo "${HOME}/.ansible/playbooks/${PLAYBOOK_NAME}"
    fi
}

MODE="$(detect_mode)"

if [[ "$MODE" == "installed" ]]; then
    INSTALL_DIR="${SCRIPT_DIR}"
else
    INSTALL_DIR="${INSTALL_DIR:-$(detect_default_location)}"
fi

# ---------- path helpers -------------------------------------
vars_file()  { echo "${INSTALL_DIR}/vars.env"; }
vault_file() { echo "${INSTALL_DIR}/${VAULT_FILE_REL}"; }

# ---------- vault security — cleanup unencrypted file --------
_VAULT_GUARD=""   # set to vault path while it may be plaintext

_vault_cleanup() {
    if [[ -n "$_VAULT_GUARD" && -f "$_VAULT_GUARD" ]]; then
        if ! head -1 "$_VAULT_GUARD" 2>/dev/null | grep -q '^\$ANSIBLE_VAULT'; then
            warn "Removing unencrypted vault.yml (interrupted operation)"
            if command -v shred &>/dev/null; then
                shred -uz "$_VAULT_GUARD" 2>/dev/null || rm -f "$_VAULT_GUARD"
            else
                rm -f "$_VAULT_GUARD"
            fi
        fi
    fi
}

_on_interrupt() {
    echo  # newline after ^C on terminal
    info "Interrupted."
    exit 130
}

# EXIT runs _vault_cleanup (covers all exit paths including INT/TERM)
# INT/TERM call _on_interrupt which triggers exit → EXIT fires cleanup
trap '_vault_cleanup' EXIT
trap '_on_interrupt'  INT TERM HUP

# ---------- load / save vars ---------------------------------
load_vars() {
    local vf; vf="$(vars_file)"
    [[ -f "$vf" ]] && source "$vf" || true  # shellcheck disable=SC1090
}

save_vars() {
    local vf; vf="$(vars_file)"
    cat > "$vf" <<EOF
# tacacs-ng configuration — generated by ${SCRIPT_NAME}
# Do not commit to git

# --- Target host ---
ANSIBLE_TARGET_HOST="${ANSIBLE_TARGET_HOST:-}"
ANSIBLE_SSH_USER="${ANSIBLE_SSH_USER:-root}"
ANSIBLE_SSH_PORT="${ANSIBLE_SSH_PORT:-22}"
ANSIBLE_SSH_KEY="${ANSIBLE_SSH_KEY:-}"
ANSIBLE_BECOME="${ANSIBLE_BECOME:-false}"
ANSIBLE_BECOME_PASSWORD="${ANSIBLE_BECOME_PASSWORD:-}"

# --- Vault ---
VAULT_PASSWORD_FILE="${VAULT_PASSWORD_FILE:-}"

# --- TACACS+ functional test ---
TACACS_RO_PASSWORD="${TACACS_RO_PASSWORD:-}"
EOF
    chmod 600 "$vf"
    ok "Configuration saved: $vf"
}

# ---------- dependency check ---------------------------------
check_dependencies() {
    header "Checking dependencies"
    local all_ok=true

    for cmd_desc in \
        "ansible-playbook:ansible-playbook (apt install ansible)" \
        "ansible-vault:ansible-vault (part of ansible)" \
        "python3:python3 (apt install python3)" \
        "openssl:openssl (apt install openssl)"; do
        local cmd="${cmd_desc%%:*}"
        local desc="${cmd_desc#*:}"
        if command -v "$cmd" &>/dev/null; then
            ok "${cmd}: found"
        else
            error "${cmd} not found — install: ${desc}"
            all_ok=false
        fi
    done

    if ansible-galaxy collection list 2>/dev/null | grep -q "community.general"; then
        ok "community.general: present"
    else
        warn "community.general not found — installing..."
        if ansible-galaxy collection install community.general; then
            ok "community.general: installed"
        else
            error "Failed to install community.general"
            all_ok=false
        fi
    fi

    [[ "$all_ok" == true ]]
}

# =============================================================
# VAULT PASSWORD
# =============================================================

find_vault_password_files() {
    # Returns list of existing vault password files, deduplicated
    local candidates=()

    # 1. Already configured in vars.env
    if [[ -n "${VAULT_PASSWORD_FILE:-}" && -f "${VAULT_PASSWORD_FILE}" ]]; then
        candidates+=("${VAULT_PASSWORD_FILE}")
    fi

    # 2. From ansible.cfg files (playbook-local, user, system)
    local cfgs=(
        "${INSTALL_DIR}/playbook/ansible.cfg"
        "${HOME}/.ansible.cfg"
        "/etc/ansible/ansible.cfg"
    )
    for cfg in "${cfgs[@]}"; do
        if [[ -f "$cfg" ]]; then
            local cfg_vp
            cfg_vp="$(grep -E '^\s*vault_password_file\s*=' "$cfg" 2>/dev/null \
                      | sed 's/.*=\s*//' | tr -d ' \t' | head -1 || true)"
            cfg_vp="${cfg_vp/#\~/$HOME}"
            [[ -n "$cfg_vp" && -f "$cfg_vp" ]] && candidates+=("$cfg_vp")
        fi
    done

    # 3. Common locations
    local common=(
        "${HOME}/.ansible/vault_pass"
        "${HOME}/.ansible/vault_password"
        "${HOME}/.vault_pass"
        "${HOME}/.vault_password"
        "/etc/ansible/vault/.vault_pass"
        "/etc/ansible/vault/vault_pass"
    )
    for f in "${common[@]}"; do
        [[ -f "$f" ]] && candidates+=("$f")
    done

    # Deduplicate preserving order
    local seen=()
    for f in "${candidates[@]:-}"; do
        local dup=false
        for s in "${seen[@]:-}"; do [[ "$s" == "$f" ]] && dup=true && break; done
        [[ "$dup" == false ]] && seen+=("$f")
    done

    printf '%s\n' "${seen[@]:-}"
}

# Sets globals: VAULT_PASSWORD_FILE (path or empty), VAULT_RAW_PASSWORD (if manual, no file)
configure_vault_password() {
    header "Vault password"
    info "The vault password will be used to encrypt vault.yml."
    echo

    VAULT_RAW_PASSWORD=""

    # Collect all known candidates
    local candidates=()
    while IFS= read -r line; do
        [[ -n "$line" ]] && candidates+=("$line")
    done < <(find_vault_password_files)

    # Build menu
    local options=()
    local i=1

    if [[ ${#candidates[@]} -gt 0 ]]; then
        for f in "${candidates[@]}"; do
            echo -e "  ${BOLD}${i})${NC} Use existing file: ${CYAN}${f}${NC}"
            options+=("$f")
            i=$(( i + 1 ))
        done
        echo
    fi

    echo -e "  ${BOLD}${i})${NC} Specify a different file path"
    options+=("__custom_path__")
    i=$(( i + 1 ))
    echo -e "  ${BOLD}${i})${NC} Enter password manually (offer to save to file)"
    options+=("__manual__")
    local max=$i

    echo
    local choice idx selected
    while true; do
        echo -ne "Choice [1-${max}]: "
        read -r choice || _on_interrupt
        # Validate: must be a number in range
        if [[ "$choice" =~ ^[0-9]+$ ]]; then
            idx=$(( choice - 1 ))
            if [[ $idx -ge 0 && $idx -lt ${#options[@]} ]]; then break; fi
        fi
        warn "Please enter a number between 1 and ${max}."
    done

    selected="${options[$idx]}"

    case "$selected" in
        __custom_path__)
            local default_path="${HOME}/.ansible/vault_pass"
            prompt VAULT_PASSWORD_FILE "Path to vault password file" "$default_path"
            VAULT_PASSWORD_FILE="${VAULT_PASSWORD_FILE/#\~/$HOME}"
            if [[ ! -f "$VAULT_PASSWORD_FILE" ]]; then
                warn "File not found: ${VAULT_PASSWORD_FILE}"
                warn "Ensure the file exists and contains the vault password before running."
            else
                ok "Will use: ${VAULT_PASSWORD_FILE}"
            fi
            ;;
        __manual__)
            _vault_password_manual
            ;;
        *)
            # Existing file selected
            if [[ ! -f "$selected" ]]; then
                die "Vault password file not found: '${selected}'"
            fi
            VAULT_PASSWORD_FILE="$selected"
            ok "Will use: ${VAULT_PASSWORD_FILE}"
            ;;
    esac
}

_vault_password_manual() {
    local pw pw2
    while true; do
        prompt_secret pw "Enter vault password"
        [[ -z "$pw" ]] && { warn "Password cannot be empty."; continue; }
        prompt_secret pw2 "Confirm vault password"
        [[ "$pw" == "$pw2" ]] && break
        warn "Passwords do not match. Try again."
    done
    VAULT_RAW_PASSWORD="$pw"

    echo
    warn "IMPORTANT: If you lose the vault password, secrets cannot be recovered."
    echo
    if confirm "Save password to a file for future use?"; then
        local default_path="${HOME}/.ansible/vault_pass"
        prompt VAULT_PASSWORD_FILE "Path to save password file" "$default_path"
        VAULT_PASSWORD_FILE="${VAULT_PASSWORD_FILE/#\~/$HOME}"
        mkdir -p "$(dirname "$VAULT_PASSWORD_FILE")"
        printf '%s' "$pw" > "$VAULT_PASSWORD_FILE"
        chmod 600 "$VAULT_PASSWORD_FILE"
        ok "Password saved: ${VAULT_PASSWORD_FILE}"
        warn "Back up this file to a password manager."
        VAULT_RAW_PASSWORD=""  # no longer needed, file takes precedence
    else
        VAULT_PASSWORD_FILE=""
        warn "No file saved — you will be prompted for the password on every run."
    fi
}

# Resolve vault pass args for ansible-playbook
vault_args() {
    # Use --vault-id to explicitly name the identity and avoid conflicts
    # with system-level ansible.cfg that may define its own vault passwords.
    if [[ -n "${VAULT_PASSWORD_FILE:-}" && -f "${VAULT_PASSWORD_FILE}" ]]; then
        printf '%s\n' "--vault-id" "default@${VAULT_PASSWORD_FILE}"
    elif [[ -n "${VAULT_RAW_PASSWORD:-}" ]]; then
        printf '%s\n' "--vault-id" "default@/dev/stdin"
    else
        printf '%s\n' "--ask-vault-pass"
    fi
}

# Pipe vault password to stdin if needed
vault_stdin_pipe() {
    if [[ -n "${VAULT_RAW_PASSWORD:-}" ]]; then
        printf '%s' "${VAULT_RAW_PASSWORD}"
    fi
}

