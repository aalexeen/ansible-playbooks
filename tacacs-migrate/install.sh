configure_test_user() {
    header "TACACS+ test user"
    info "A TACACS+ user to verify authentication after migration."
    info "Should be a readonly user (safe — cannot make changes)."
    echo

    prompt TACACS_TEST_USER "TACACS+ test username" "${TACACS_TEST_USER:-htc-ro}"
    if [[ -z "${TACACS_TEST_USER:-}" ]]; then die "Aborted: TACACS+ test username is required and cannot be empty. It is used to verify authentication after migration."; fi

    prompt_secret TACACS_TEST_PASS "TACACS+ test user password"
    TACACS_TEST_PASS="${TACACS_TEST_PASS:-}"

    if [[ -z "${TACACS_TEST_PASS:-}" ]]; then
