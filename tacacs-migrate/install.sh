configure_hosts() {
    header "Configure switch inventory"
    info "Add switches to migrate."
    info "Format: hostname IP (e.g. SW01 192.168.1.10)"
    info "IOS version is auto-detected during preflight — no grouping needed."
    echo

    local switches=""

    info "Enter one switch per line. Press Enter on empty line when done."
    info "Accepted formats: 'SW01 192.168.1.10' or just '192.168.1.10' (IP used as hostname)"
    while true; do
        echo -ne "  ${BOLD}[hostname] IP${NC} (empty to finish): "
        read -r line || _on_interrupt
        [[ -z "$line" ]] && break
        local hname hip
        hname="$(echo "$line" | awk '{print $1}')"
        hip="$(echo "$line" | awk '{print $2}')"
        if [[ -z "$hip" ]]; then
            # Only one token entered — treat it as IP, use IP as hostname
            hip="$hname"
            hname="$hip"
        fi
        switches+="${hname} ansible_host=${hip}"$'\n'
    done

    if [[ -z "$switches" ]]; then
        warn "No switches entered. Edit ${INSTALL_DIR}/inventory/hosts manually."
    fi

    _write_inventory "$switches"
}

_write_inventory() {
    local switches="$1"
    mkdir -p "${INSTALL_DIR}/inventory"

    {
        echo "# tacacs-migrate inventory"
        echo "# Generated by ${SCRIPT_NAME}"
        echo "# IOS version is auto-detected during preflight — no grouping needed."
        echo ""
        echo "[switches]"
        if [[ -n "$switches" ]]; then
            echo "$switches"
        else
            echo "# SW01 ansible_host=10.0.0.1"
            echo "# SW02 ansible_host=10.0.0.2"
        fi
        echo ""
        echo "[all:vars]"
        echo "ansible_user={{ local_user }}"
        echo "ansible_password={{ local_pass }}"
    } > "${INSTALL_DIR}/inventory/hosts"

    ok "Inventory written: ${INSTALL_DIR}/inventory/hosts"
}

_write_ansible_cfg() {
    local vault_line=""
    if [[ -n "${VAULT_PASSWORD_FILE:-}" && -f "${VAULT_PASSWORD_FILE}" ]]; then
        vault_line="vault_password_file = ${VAULT_PASSWORD_FILE}"
    fi

    cat > "${INSTALL_DIR}/playbook/ansible.cfg" <<EOF
[defaults]
roles_path           = ../roles
inventory            = ../inventory/hosts
host_key_checking    = False
stdout_callback      = yaml
retry_files_enabled  = False
${vault_line}

[persistent_connection]
connect_timeout      = 30
command_timeout      = 60
EOF
    ok "ansible.cfg updated: ${INSTALL_DIR}/playbook/ansible.cfg"
}

# =============================================================
# FULL CONFIGURE
# =============================================================

configure_vault() {
    header "Configure TACACS+ migration secrets"
    load_vars

    local vf; vf="$(vault_file)"
    if [[ -f "$vf" ]]; then
        warn "An existing vault.yml was found. It will be replaced."
        if ! confirm "Continue?"; then info "Cancelled."; return; fi
    fi

    configure_broker
    configure_servers
    configure_switch_credentials
    configure_test_user
    configure_vault_password
    create_and_encrypt_vault
    _write_ansible_cfg
    save_vars

    echo
    ok "Vault configured and encrypted."
    if [[ -n "${TACACS_SERVER_SECONDARY:-}" ]]; then
        info "Servers: ${TACACS_SERVER_PRIMARY} (primary), ${TACACS_SERVER_SECONDARY} (secondary)"
    else
        info "Server:  ${TACACS_SERVER_PRIMARY} (single server mode)"
    fi
}

do_configure() {
    configure_hosts
    configure_vault
}

do_configure_broker() {
    header "Configure ssh-broker in vault"

    local vf; vf="$(vault_file)"
    [[ -f "$vf" ]] || die "vault.yml not found. Run 'configure-vault' first."

    load_vars

    # Decrypt vault to a temp file
    local tmp; tmp="$(mktemp)"
    chmod 600 "$tmp"
    _VAULT_GUARD="$tmp"

    local va=()
    mapfile -t va < <(vault_args)

    if [[ "${va[1]:-}" == "default@/dev/stdin" ]]; then
        vault_stdin_pipe | ansible-vault decrypt "$vf" "${va[@]}" --output "$tmp" \
            || die "Failed to decrypt vault.yml"
    else
        ansible-vault decrypt "$vf" "${va[@]}" --output "$tmp" \
            || die "Failed to decrypt vault.yml"
    fi

    configure_broker

    # Update broker_url and broker_token lines in the decrypted file.
    # If lines exist — replace them; if not — append broker section.
    if grep -q "^broker_url:" "$tmp" 2>/dev/null; then
        sed -i "s|^broker_url:.*|broker_url:   \"${BROKER_URL}\"|" "$tmp"
        sed -i "s|^broker_token:.*|broker_token: \"${BROKER_TOKEN}\"|" "$tmp"
    else
        printf '\n# ssh-broker safety session\nbroker_url:   "%s"\nbroker_token: "%s"\n' \
            "${BROKER_URL}" "${BROKER_TOKEN}" >> "$tmp"
    fi

    # Re-encrypt
    if [[ -n "${VAULT_PASSWORD_FILE:-}" && -f "${VAULT_PASSWORD_FILE}" ]]; then
        ansible-vault encrypt "$tmp" \
            --vault-id "default@${VAULT_PASSWORD_FILE}" \
            --encrypt-vault-id default \
            --output "$vf"
    elif [[ -n "${VAULT_RAW_PASSWORD:-}" ]]; then
        printf '%s' "${VAULT_RAW_PASSWORD}" \
            | ansible-vault encrypt "$tmp" \
                --vault-id "default@/dev/stdin" \
                --encrypt-vault-id default \
                --output "$vf"
    else
        ansible-vault encrypt "$tmp" --encrypt-vault-id default --output "$vf"
    fi

    rm -f "$tmp"
    _VAULT_GUARD=""
    ok "Broker settings saved and vault re-encrypted."
    info "Verify with: ansible-vault view ${VAULT_FILE_REL}"
}

# =============================================================
# INSTALL
# =============================================================

do_install() {
    [[ "$MODE" == "source" ]] || die "install can only be run from the source directory."

    header "Installing ${PLAYBOOK_NAME}"
    check_dependencies || die "Dependency check failed."

    local default_loc; default_loc="$(detect_default_location)"
    prompt INSTALL_DIR "Install location" "$default_loc"
    export INSTALL_DIR

    if [[ -f "${INSTALL_DIR}/.installed" ]]; then
        warn "Already installed at: ${INSTALL_DIR}"
        if ! confirm "Reinstall (overwrite)?"; then info "Aborted."; return; fi
    fi

    info "Copying playbook files to ${INSTALL_DIR}..."
    mkdir -p "${INSTALL_DIR}"

    cp -r "${SCRIPT_DIR}/playbook" "${INSTALL_DIR}/"
    cp -r "${SCRIPT_DIR}/roles"    "${INSTALL_DIR}/"
    mkdir -p "${INSTALL_DIR}/inventory"
    cp "${SCRIPT_DIR}/inventory/hosts" "${INSTALL_DIR}/inventory/hosts"

    # Remove vault.yml if it was accidentally included
    rm -f "${INSTALL_DIR}/${VAULT_FILE_REL}"

    ok "Files copied."

    cp "${BASH_SOURCE[0]}" "${INSTALL_DIR}/tacacs-migrate.sh"
    chmod +x "${INSTALL_DIR}/tacacs-migrate.sh"
    ok "Management script installed: ${INSTALL_DIR}/tacacs-migrate.sh"

    [[ -f "${SCRIPT_DIR}/USAGE.md" ]] && cp "${SCRIPT_DIR}/USAGE.md" "${INSTALL_DIR}/USAGE.md"

    MODE="installed"
    INSTALL_DIR="${INSTALL_DIR}"
    touch "${INSTALL_DIR}/.installed"

    do_configure

    echo
    ok "Installation complete."
    echo
    info "Manage your deployment:"
    echo -e "  ${CYAN}${INSTALL_DIR}/tacacs-migrate.sh${NC}                — interactive menu"
    echo -e "  ${CYAN}${INSTALL_DIR}/tacacs-migrate.sh run${NC}             — run migration"
    echo -e "  ${CYAN}${INSTALL_DIR}/tacacs-migrate.sh run -l SW01${NC}     — migrate single switch"
    echo -e "  ${CYAN}${INSTALL_DIR}/tacacs-migrate.sh configure${NC}       — reconfigure"
    echo
}

# =============================================================
# RUN
# =============================================================

do_run() {
    header "Running migration playbook"

    [[ -f "${INSTALL_DIR}/.installed" ]] || die "Not installed. Run: $0 install"

    load_vars

    local vf; vf="$(vault_file)"
    [[ -f "$vf" ]] || die "vault.yml not found. Run: $0 configure-vault"

    # ssh-broker pre-flight check
    local _broker_url="${BROKER_URL:-http://127.0.0.1:8765}"
    if command -v curl &>/dev/null; then
        if curl -sf --max-time 2 "${_broker_url}/health" &>/dev/null; then
            ok "ssh-broker: reachable at ${_broker_url}"
        else
            warn "ssh-broker is NOT reachable at ${_broker_url}"
            warn "Migration will abort per-host at phase 0 (broker open)."
            warn "Start ssh-broker or configure broker settings: $0 configure-broker"
            echo
            if ! confirm "Continue anyway?"; then info "Aborted."; return; fi
        fi
    fi

    local va=()
    mapfile -t va < <(vault_args)

    local extra_args=()

    info "ansible-playbook migrate.yml ${va[*]:-} ${extra_args[*]:-} ${EXTRA_ARGS[*]:-}"
    cd "${INSTALL_DIR}/playbook"

    if [[ "${va[1]:-}" == "default@/dev/stdin" ]]; then
        vault_stdin_pipe | ansible-playbook migrate.yml \
            "${va[@]}" "${extra_args[@]}" "${EXTRA_ARGS[@]}"
    else
        ansible-playbook migrate.yml \
            "${va[@]}" "${extra_args[@]}" "${EXTRA_ARGS[@]}"
    fi
}

# =============================================================
# STATUS
# =============================================================

show_status() {
    header "Status"

    if [[ "$MODE" == "source" ]]; then
        info "Running in SOURCE mode: ${SCRIPT_DIR}"
        if [[ -f "${INSTALL_DIR}/.installed" ]]; then
            ok "Installed at: ${INSTALL_DIR}"
        else
            warn "Not installed. Run: $0 install"
        fi
        return
    fi

    ok "Install dir: ${INSTALL_DIR}"
    echo

    local vf; vf="$(vars_file)"
    if [[ -f "$vf" ]]; then
        load_vars
        echo -e "  ${BOLD}Vault pass file${NC} : ${VAULT_PASSWORD_FILE:-(will prompt)}"
    else
        warn "Not configured. Run: $0 configure"
    fi

    echo
    local inv="${INSTALL_DIR}/inventory/hosts"
    if [[ -f "$inv" ]]; then
        local sw_count
        sw_count="$(grep -cE '^[A-Za-z].*ansible_host=' "$inv" 2>/dev/null || echo 0)"
        ok "Inventory : ${sw_count} switch(es)"
        # Show non-commented hosts
        grep -E '^[A-Za-z].*ansible_host=' "$inv" 2>/dev/null | \
            sed 's/^/    /' || true
    else
        warn "Inventory not found — run: $0 configure-hosts"
    fi

    echo
    local vault_f; vault_f="$(vault_file)"
    if [[ -f "$vault_f" ]]; then
        if head -1 "$vault_f" 2>/dev/null | grep -q '^\$ANSIBLE_VAULT'; then
            ok "vault.yml : encrypted"
            # Show server config from vault (view without decrypting beyond header)
            info "To view vault contents: ansible-vault view ${VAULT_FILE_REL}"
        else
            warn "vault.yml : EXISTS BUT NOT ENCRYPTED — run: $0 configure-vault"
        fi
    else
        warn "vault.yml : not found — run: $0 configure-vault"
    fi
}

# =============================================================
# UNINSTALL
# =============================================================

do_uninstall() {
    header "Uninstalling ${PLAYBOOK_NAME}"
    [[ -f "${INSTALL_DIR}/.installed" ]] || { warn "Not installed."; return; }
    warn "This will permanently remove: ${INSTALL_DIR}"
    if ! confirm "Are you sure?"; then info "Aborted."; return; fi
    rm -rf "${INSTALL_DIR}"
    ok "Removed: ${INSTALL_DIR}"
}

