menu_source() {
    while true; do
        echo
        echo -e "${BOLD}=== tacacs-migrate Installer ===${NC}"
        echo -e "  Source: ${CYAN}${SCRIPT_DIR}${NC}"
        echo -e "  Install target: ${CYAN}${INSTALL_DIR}${NC}"
        echo
        echo "  1) Install"
        echo "  2) Status"
        echo "  3) Exit"
        echo
        echo -ne "Choice [1-3]: "
        read -r choice || _on_interrupt
        case "$choice" in
            1) do_install; exit 0 ;;
            2) show_status ;;
            3) exit 0 ;;
            *) warn "Invalid choice." ;;
        esac
    done
}

menu_installed() {
    while true; do
        echo
        echo -e "${BOLD}=== tacacs-migrate Management ===${NC}"
        echo -e "  ${CYAN}${INSTALL_DIR}${NC}"
        echo
        echo "  1) Run migration playbook"
        echo "  2) Configure everything (hosts + vault)"
        echo "  3) Configure switches (inventory) only"
        echo "  4) Configure TACACS+ secrets only (recreate vault)"
        echo "  5) Configure ssh-broker settings (update vault)"
        echo "  6) Change vault password"
        echo "  7) Status"
        echo "  8) Uninstall"
        echo "  9) Exit"
        echo
        echo -ne "Choice [1-9]: "
        read -r choice || _on_interrupt
        case "$choice" in
            1) do_run ;;
            2) do_configure ;;
            3) configure_hosts ;;
            4) configure_vault ;;
            5) do_configure_broker ;;
            6) do_configure_vault_password ;;
            7) show_status ;;
            8) do_uninstall ;;
            9) exit 0 ;;
            *) warn "Invalid choice." ;;
        esac
    done
}

