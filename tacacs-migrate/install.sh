configure_switch_credentials() {
    header "Switch credentials (local auth)"
    info "These credentials are used to connect to switches for migration."
    info "They must be LOCAL admin credentials (not TACACS+)."
    echo

    prompt LOCAL_USER   "Switch local username"        "${LOCAL_USER:-admin}"
    prompt_secret LOCAL_PASS         "Switch local password"
    prompt_secret LOCAL_ENABLE       "Switch enable secret (level 15, leave empty if not needed)"

    echo
    info "The admin user will be recreated with privilege 15 and an encrypted secret during preflight."
    prompt_secret LOCAL_ADMIN_SECRET "Switch admin user secret (type 9, for 'username admin privilege 15 secret')"
    if [[ -z "${LOCAL_ADMIN_SECRET:-}" ]]; then die "Aborted: admin secret is required and cannot be empty. It is used during preflight to ensure the admin user has privilege 15."; fi

    ok "Switch credentials configured for user: ${LOCAL_USER}"
}

