case "$ACTION" in
    install)
        do_install
        ;;
    configure)
        [[ "$MODE" == "installed" ]] || die "'configure' requires installed mode. Run install first."
        do_configure
        ;;
    configure-hosts)
        [[ "$MODE" == "installed" ]] || die "'configure-hosts' requires installed mode."
