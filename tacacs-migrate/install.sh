    fi
}

# =============================================================
# SSH-BROKER CONFIGURATION
# =============================================================

configure_broker() {
    header "Configure ssh-broker safety session"
    info "ssh-broker provides a persistent SSH safety session for rollback."
    info "It must be running on this controller before migration starts."
    echo

    local default_url="${BROKER_URL:-http://127.0.0.1:8765}"
    prompt BROKER_URL   "ssh-broker URL"       "$default_url"
    prompt_secret BROKER_TOKEN "ssh-broker API token"

    if [[ -z "${BROKER_TOKEN:-}" ]]; then
        die "Aborted: broker API token is required and cannot be empty."
    fi

    # Verify broker is reachable
    echo
    if command -v curl &>/dev/null; then
        if curl -sf --max-time 3 "${BROKER_URL}/health" &>/dev/null; then
            ok "ssh-broker is reachable at ${BROKER_URL}"
        else
            warn "Could not reach ssh-broker at ${BROKER_URL}"
            warn "Make sure ssh-broker is running before executing the migration."
        fi
    fi

    ok "Broker settings configured."
}

# =============================================================
# VAULT CREATE / REKEY
# =============================================================

create_and_encrypt_vault() {
    local vf; vf="$(vault_file)"
    mkdir -p "$(dirname "$vf")"

    if [[ -f "$vf" ]]; then
        local backup="${vf}.bak.$(date +%Y%m%d%H%M%S)"
        mv "$vf" "$backup"
        info "Previous vault backed up: ${backup}"
    fi

    info "Writing vault.yml..."

    cat > "$vf" <<EOF
---
# vault.yml — TACACS+ migration secrets, encrypted with ansible-vault
# DO NOT commit this file to git
# To edit: ansible-vault edit ${VAULT_FILE_REL}
# To view: ansible-vault view ${VAULT_FILE_REL}

# ssh-broker safety session
broker_url:   "${BROKER_URL:-http://127.0.0.1:8765}"
broker_token: "${BROKER_TOKEN:-}"

# Local switch credentials (used by migration + rollback safety session)
local_user:          "${LOCAL_USER}"
local_pass:          "${LOCAL_PASS}"
local_enable:        "${LOCAL_ENABLE:-}"
local_admin_secret:  "${LOCAL_ADMIN_SECRET}"

# TACACS+ shared key — shared secret configured on your TACACS+ servers
tacacs_key: "${TACACS_KEY}"

# TACACS+ test user for verification after migration (readonly user)
tacacs_test_user: "${TACACS_TEST_USER}"
tacacs_test_pass: "${TACACS_TEST_PASS:-}"

# TACACS+ servers
tacacs_server_primary:   "${TACACS_SERVER_PRIMARY}"
tacacs_server_secondary: "${TACACS_SERVER_SECONDARY:-}"
EOF
    chmod 600 "$vf"
    _VAULT_GUARD="$vf"

    info "Encrypting vault.yml..."

    if [[ -n "${VAULT_PASSWORD_FILE:-}" && -f "${VAULT_PASSWORD_FILE}" ]]; then
        ansible-vault encrypt "$vf" \
            --vault-id "default@${VAULT_PASSWORD_FILE}" \
            --encrypt-vault-id default
    elif [[ -n "${VAULT_RAW_PASSWORD:-}" ]]; then
        printf '%s' "${VAULT_RAW_PASSWORD}" \
            | ansible-vault encrypt "$vf" \
                --vault-id "default@/dev/stdin" \
                --encrypt-vault-id default
    else
        ansible-vault encrypt "$vf" --encrypt-vault-id default
    fi

    _VAULT_GUARD=""
    ok "vault.yml encrypted."
}

do_configure_vault_password() {
    header "Change vault password"

    local vf; vf="$(vault_file)"
    [[ -f "$vf" ]] || die "vault.yml not found. Run 'configure-vault' first."

    info "You will need the CURRENT vault password, then set a NEW one."
    echo

    local cur_file="" cur_raw=""
    load_vars
    if [[ -n "${VAULT_PASSWORD_FILE:-}" && -f "${VAULT_PASSWORD_FILE}" ]]; then
        info "Current vault password file: ${VAULT_PASSWORD_FILE}"
        if ! confirm "Use this file for current password?"; then
            prompt_secret cur_raw "Current vault password"
        else
            cur_file="${VAULT_PASSWORD_FILE}"
        fi
    else
        prompt_secret cur_raw "Current vault password"
    fi

    echo
    info "Now configure the NEW vault password:"
    VAULT_PASSWORD_FILE=""
    VAULT_RAW_PASSWORD=""
    _vault_password_manual

    local cur_args=()
    if [[ -n "$cur_file" ]]; then
        cur_args=(--vault-password-file "$cur_file")
    else
        cur_args=(--vault-password-file /dev/stdin)
    fi

    local new_args=()
    if [[ -n "${VAULT_PASSWORD_FILE:-}" ]]; then
        new_args=(--new-vault-password-file "${VAULT_PASSWORD_FILE}")
    else
        local tmp; tmp="$(mktemp)"
        printf '%s' "${VAULT_RAW_PASSWORD}" > "$tmp"
        chmod 600 "$tmp"
        new_args=(--new-vault-password-file "$tmp")
        trap "rm -f '$tmp'" RETURN
    fi

    if [[ -n "$cur_raw" ]]; then
        printf '%s' "$cur_raw" \
            | ansible-vault rekey "$vf" "${cur_args[@]}" "${new_args[@]}"
    else
        ansible-vault rekey "$vf" "${cur_args[@]}" "${new_args[@]}"
    fi

    ok "Vault password changed."
    save_vars
}

