# TACACS+ SERVERS CONFIGURATION
# =============================================================

configure_servers() {
    header "Configure TACACS+ servers"
    info "Enter primary and (optionally) secondary TACACS+ server."
    info "Leave secondary empty to use a single server."
    echo

    prompt TACACS_SERVER_PRIMARY "Primary TACACS+ server IP" "${TACACS_SERVER_PRIMARY:-}"
    if [[ -z "${TACACS_SERVER_PRIMARY:-}" ]]; then die "Aborted: primary TACACS+ server IP is required and cannot be empty."; fi

    echo
    prompt TACACS_SERVER_SECONDARY "Secondary TACACS+ server IP (leave empty for single server)" \
        "${TACACS_SERVER_SECONDARY:-}"

    if [[ -n "${TACACS_SERVER_SECONDARY:-}" ]]; then
        ok "Two servers configured: ${TACACS_SERVER_PRIMARY}, ${TACACS_SERVER_SECONDARY}"
    else
        ok "Single server configured: ${TACACS_SERVER_PRIMARY}"
    fi

    echo
    echo -e "${BOLD}-- TACACS+ shared key --${NC}"
    prompt_secret TACACS_KEY "TACACS+ shared key (shared secret configured on your TACACS+ servers)"
    if [[ -z "${TACACS_KEY:-}" ]]; then die "Aborted: TACACS+ shared key is required and cannot be empty."; fi
}

