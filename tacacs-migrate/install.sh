        do_configure_vault_password
        ;;
    run)
        do_run
        ;;
    status)
        show_status
        ;;
    uninstall)
        do_uninstall
        ;;
    menu)
        if [[ "$MODE" == "source" ]]; then
            menu_source
        else
            menu_installed
        fi
        ;;
    help|--help|-h)
        echo
        echo -e "${BOLD}Usage:${NC} $0 <command> [args]"
        echo
        if [[ "$MODE" == "source" ]]; then
            echo "  install                    Install playbook to target directory"
            echo "  status                     Show installation status"
        else
            echo "  configure                  Configure hosts + secrets (full setup)"
            echo "  configure-hosts            Configure switch inventory only"
            echo "  configure-vault            Configure TACACS+ secrets (recreate vault)"
            echo "  configure-broker           Configure ssh-broker URL and token (update vault)"
            echo "  configure-vault-password   Change vault encryption password"
            echo "  run [--limit HOST] [args]  Run migration playbook"
            echo "  status                     Show current configuration"
            echo "  uninstall                  Remove installed playbook"
        fi
        echo "  help                       Show this message"
        echo
        echo -e "Current mode: ${CYAN}${MODE}${NC}  |  Install dir: ${CYAN}${INSTALL_DIR}${NC}"
        echo
        echo -e "${BOLD}Run examples:${NC}"
        echo "  $0 run                        — migrate all switches (serial=1)"
        echo "  $0 run -l AA01ASWI            — migrate single switch"
        echo "  $0 run -e serial=5            — migrate 5 switches in parallel"
        echo "  $0 run --tags step1,step2     — run specific steps only"
        echo
        ;;
    *)
        error "Unknown command: ${ACTION}"
        echo "Run '$0 help' for usage."
        exit 1
        ;;
esac
